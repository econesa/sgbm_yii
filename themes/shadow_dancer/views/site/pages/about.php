<h1>Nosotros</h1>

<h2>Misión</h2>

<p>Proteger a todos los ciudadanos, en el ámbito Geográfico  del municipio sucre de todas aquellas Situaciones que puedan constituirse en amenazas, Vulnerabilidades o riesgos para su integridad física y la de sus bienes, garantizar la convivencia, la supremacía de la ley, la prevalencia del bienestar común y La participación ciudadana.</p>

<h2>Visión</h2>

<p>Ser la institución policial modelo, reconocida por su vocación de servicio, honestidad y eficiencia, cuya labor integrada a los requerimientos de la comunidad del municipio sucre, sea capaz de satisfacer las necesidades de protección contra la delincuencia y el pleno disfrute de los derechos humanos.</p>

<h2>Valores institucionales</h2>

<h3>Respeto</h3>

<p>Basado en el reconocimiento del ser humano y en el reconocimiento de sus derechos fundamentales como lo son el derecho a la vida, el derecho a disfrutar de su libertad, el derecho a disponer de sus propiedades o el derecho a proteger su intimidad, incluyendo el respeto al medio ambiente, a los seres vivos y a la naturaleza en general, sin olvidar el respeto a las Leyes, a las normas sociales, a la memoria de los antepasados y a la Patria en que nacimos.</p>

<h3>Compromiso</h3>

<p>Entendido como la obligación de convertir en realidad nuestro juramento de promover la seguridad en el ámbito de nuestro Municipio Sucre mediante acciones, aún cuando estén presentes circunstancias adversas.</p>

<h3>Solidaridad</h3>

<p>Como principio fundamental de la convivencia, este Valor nos conduce a actuar comprensivamente ante las necesidades de apoyo de los demás miembros de nuestro Municipio y a sensibilizarnos con la realidad política, económica, social y cultural de nuestro país.</p>

<h3>Integridad</h3>

<p>Nuestra Institución practica lo que predica, predica con el ejemplo y reconoce la importancia de la práctica de los Valores.</p>

<h3>Responsabilidad</h3>

<p>Concebida como el ejercicio pleno y consciente de las libertades personales y también de las consecuencias de nuestras acciones u omisiones.</p>

<h3>Honestidad</h3>

<p>Para lograr que la confianza colectiva en nuestros proyectos se transforme en una fuerza de gran valor, nuestra conducta debe ser transparente.</p>
